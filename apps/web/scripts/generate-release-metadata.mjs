import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repositoryOwner = "Benjam11n";
const repositoryName = "zucchini";
const repositoryUrl = `https://github.com/${repositoryOwner}/${repositoryName}`;
const releasesUrl = `${repositoryUrl}/releases`;
const latestReleaseUrl = `${releasesUrl}/latest`;
const githubApiUrl = `https://api.github.com/repos/${repositoryOwner}/${repositoryName}/releases?per_page=20`;

const webRoot = path.resolve(import.meta.dirname, "..");
const workspaceRoot = path.resolve(webRoot, "../..");
const desktopPackagePath = path.join(
  workspaceRoot,
  "apps",
  "desktop",
  "package.json"
);
const outputPath = path.join(
  webRoot,
  "src",
  "generated",
  "release-metadata.ts"
);

function stripVersionPrefix(tagName) {
  return tagName.startsWith("v") ? tagName.slice(1) : tagName;
}

function scoreMacAsset(asset) {
  const name = asset.name.toLowerCase();

  if (!name.endsWith(".dmg")) {
    return -1;
  }

  let score = 100;

  if (name.includes("arm64")) {
    score += 30;
  }

  if (name.includes("universal")) {
    score += 20;
  }

  if (name.includes("mac")) {
    score += 10;
  }

  return score;
}

function selectMacDownloadUrl(assets) {
  return (
    assets
      .map((asset) => ({
        score: scoreMacAsset(asset),
        url: asset.browser_download_url,
      }))
      .filter((asset) => asset.score >= 0 && asset.url)
      .toSorted((left, right) => right.score - left.score)[0]?.url ?? null
  );
}

async function readDesktopVersion() {
  const packageJson = JSON.parse(await readFile(desktopPackagePath, "utf-8"));
  return typeof packageJson.version === "string" ? packageJson.version : null;
}

async function fetchLatestPublishedRelease() {
  const token = process.env["GITHUB_TOKEN"] ?? process.env["GH_TOKEN"];
  const response = await fetch(githubApiUrl, {
    headers: {
      Accept: "application/vnd.github+json",
      ...(token ? { Authorization: `Bearer ${token}` } : {}),
      "User-Agent": "zucchini-web-build",
      "X-GitHub-Api-Version": "2022-11-28",
    },
    signal: AbortSignal.timeout(8000),
  });

  if (!response.ok) {
    throw new Error(`GitHub releases request failed: ${response.status}`);
  }

  const releases = await response.json();

  if (!Array.isArray(releases)) {
    throw new TypeError("GitHub releases response was not an array.");
  }

  const latestRelease = releases.find((release) => !release.draft);

  if (!latestRelease?.tag_name || !latestRelease.html_url) {
    throw new Error("No published GitHub release found.");
  }

  return {
    isFallback: false,
    macDownloadUrl: selectMacDownloadUrl(latestRelease.assets ?? []),
    publishedAt: latestRelease.published_at ?? null,
    releaseUrl: latestRelease.html_url,
    tagName: latestRelease.tag_name,
    version: stripVersionPrefix(latestRelease.tag_name),
  };
}

async function createFallbackMetadata(reason) {
  const desktopVersion = await readDesktopVersion();

  console.warn(
    `Falling back to static release metadata: ${reason instanceof Error ? reason.message : String(reason)}`
  );

  return {
    isFallback: true,
    macDownloadUrl: null,
    publishedAt: null,
    releaseUrl: latestReleaseUrl,
    tagName: desktopVersion ? `v${desktopVersion}` : null,
    version: desktopVersion,
  };
}

function renderMetadataModule(metadata) {
  const fields = [
    ["isFallback", metadata.isFallback],
    ["macDownloadUrl", metadata.macDownloadUrl],
    ["publishedAt", metadata.publishedAt],
    ["releaseUrl", metadata.releaseUrl],
    ["tagName", metadata.tagName],
    ["version", metadata.version],
  ];
  const renderedFields = fields
    .map(([key, value]) => renderMetadataField(key, value))
    .join("\n");

  return `/* Generated by scripts/generate-release-metadata.mjs. Do not edit by hand. */

interface ReleaseMetadata {
  isFallback: boolean;
  macDownloadUrl: string | null;
  publishedAt: string | null;
  releaseUrl: string;
  tagName: string | null;
  version: string | null;
}

export const releaseMetadata: ReleaseMetadata = {
${renderedFields}
};
`;
}

function renderMetadataField(key, value) {
  const renderedValue = JSON.stringify(value);
  const inlineField = `  ${key}: ${renderedValue},`;

  if (inlineField.length <= 80) {
    return inlineField;
  }

  return `  ${key}:\n    ${renderedValue},`;
}

async function main() {
  let metadata;

  try {
    metadata = await fetchLatestPublishedRelease();
  } catch (error) {
    metadata = await createFallbackMetadata(error);
  }

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, renderMetadataModule(metadata));
  console.log(
    `Wrote release metadata for ${metadata.tagName ?? "latest release"}.`
  );
}

await main();
